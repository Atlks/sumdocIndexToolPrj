package com.attilax.util.cli;

public class cliRet {

	public String stdStr;
	public String errStr;
	public int exitStatCode;
	public String getStdStr() {
		return stdStr;
	}
	public void setStdStr(String stdStr) {
		this.stdStr = stdStr;
	}
	public String getErrStr() {
		return errStr;
	}
	public void setErrStr(String errStr) {
		this.errStr = errStr;
	}
	public int getExitStatCode() {
		return exitStatCode;
	}
	public void setExitStatCode(int exitStatCode) {
		this.exitStatCode = exitStatCode;
	}

}
